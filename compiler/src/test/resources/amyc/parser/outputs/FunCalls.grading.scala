object FunCalls
  fn foo(i: Int): Int = {
    i
  }
  fn bar(i: Int, j: Int): Int = {
    (i + j)
  }
  foo(1);
  val foz: Int =
    4;
  foo(foz);
  foo((
    val f: Int =
      42;
    f
  ));
  bar(1, 2);
  val baz: Int =
    4;
  foo(foz, baz);
  foo((
    val f: Int =
      42;
    f
  ), (
    val b: Int =
      1;
    b
  ))
end FunCalls